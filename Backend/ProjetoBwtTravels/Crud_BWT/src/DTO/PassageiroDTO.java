
package DTO;

public class PassageiroDTO {
    private String nomeCompleto_Passageiro, endereco_Passageiro, cep_Passageiro, email_Passageiro, contato_Passageiro, senha_Passageiro;

   
    public String getNomeCompleto_Passageiro() {
        return nomeCompleto_Passageiro;
    }

    
    public void setNomeCompleto_Passageiro(String nomeCompleto_Passageiro) {
        this.nomeCompleto_Passageiro = nomeCompleto_Passageiro;
    }

  
    public String getEndereco_Passageiro() {
        return endereco_Passageiro;
    }

   
    public void setEndereco_Passageiro(String endereco_Passageiro) {
        this.endereco_Passageiro = endereco_Passageiro;
    }

   
    public String getCep_Passageiro() {
        return cep_Passageiro;
    }

    
    public void setCep_Passageiro(String cep_Passageiro) {
        this.cep_Passageiro = cep_Passageiro;
    }

    public String getEmail_Passageiro() {
        return email_Passageiro;
    }

   
    public void setEmail_Passageiro(String email_Passageiro) {
        this.email_Passageiro = email_Passageiro;
    }

   
    public String getContato_Passageiro() {
        return contato_Passageiro;
    }

    
    public void setContato_Passageiro(String contato_Passageiro) {
        this.contato_Passageiro = contato_Passageiro;
    }

   
    public String getSenha_Passageiro() {
        return senha_Passageiro;
    }

    
    public void setSenha_Passageiro(String senha_Passageiro) {
        this.senha_Passageiro = senha_Passageiro;
    }
    
}
